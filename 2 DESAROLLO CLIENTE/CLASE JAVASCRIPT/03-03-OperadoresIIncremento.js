
let a = 3;
let b = 2;
let z = a + b;

// Incremento Suma
z = ++a; 
console.log(a);
console.log(z);


 
// Post-Incremento Resta

z = b++;
console.log(b);
console.log(z);


// Decremento
// Predecremento
z = --a;
console.log(a);
console.log(z);


// Post Decremento
z = b--;
console.log(b);
console.log(z);




























































